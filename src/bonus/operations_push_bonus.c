/*---------------------------------------------------*/
/*  Pushes the top of stack b to the top of stack a  */
/*---------------------------------------------------*/
void	pa(t_stack **a, t_stack **b)
{
	push(a, b);
}

/*---------------------------------------------------*/
/*  Pushes the top of stack a to the top of stack b  */
/*---------------------------------------------------*/
void	pb(t_stack **b, t_stack **a)
{
	push(b, a);
}
